    private void actualizarTabla() {
        DefaultTableModel data = new DefaultTableModel(new String[]{"Id", "Nombre", "Seccion", "Fecha de inicio", "Fecha de fin", "Hora inicio", "Hora fin", "Profesor"}, LoginA.cursos.size());
        jTable1.setModel(data);

        int row = 0;

        for (Curso u : LoginA.cursos) {
            jTable1.setValueAt(u.id, row, 0);
            jTable1.setValueAt(u.Nombre, row, 1);
            jTable1.setValueAt(u.Seccion, row, 2);
            jTable1.setValueAt(u.Fechadeinicio, row, 3);
            jTable1.setValueAt(u.Fechadefin, row, 4);
            jTable1.setValueAt(u.Horainicio, row, 5);
            jTable1.setValueAt(u.Horafin, row, 6);
            jTable1.setValueAt(u.Profesor, row, 7);
            row++;
        }
    }

