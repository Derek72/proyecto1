    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        JFileChooser archivo = new JFileChooser();
        int retorno = archivo.showDialog(this, "Cargar");
        if (retorno == JFileChooser.APPROVE_OPTION) {
            JSONParser jp = new JSONParser();
            try {
                FileReader r = new FileReader(archivo.getSelectedFile().toPath().toString());
                Object obj = jp.parse(r);

                JSONArray cursosList = (JSONArray) obj;

                for (Object c : cursosList) {
                    
                    JSONObject curso = (JSONObject) c;

                    String id = String.valueOf(curso.get("id"));
                    String nombreCurso = (String) curso.get("Nombre");
                    String seccion = (String) curso.get("Seccion");
                    String fechadeinicio = (String) curso.get("Fechadeinicio");
                    String fechadefin = (String) curso.get("Fechadefin");
                    String horaInicio = (String) curso.get("Horainicio");
                    String horaFin = (String) curso.get("Horafin");
                    String profesor = (String) curso.get("Profesor");
                    
                    Curso u = new Curso();
                    
                    u.id = id;
                    u.Nombre = nombreCurso;
                    u.Seccion = seccion;
                    u.Fechadeinicio = fechadeinicio;
                    u.Fechadefin = fechadefin;
                    u.Profesor = profesor;
                    u.Horainicio =  horaInicio;
                    u.Horafin = horaFin;
                    
                    LoginA.cursos.add(u);
                    
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
           
            actualizarTabla();
            JOptionPane.showMessageDialog(null, "Archivo Cargado Exitosamente");
        }

    
    }//GEN-LAST:event_jButton4ActionPerformed

